def gnis_uri(x):
    "Return a URI for a USGS Geoname."
    return 'http://data.usgs.gov/gnis/' + x

def structures_uri(x):
    "Return a URI for a USGS Structure."
    return 'http://data.usgs.gov/structures/' + x
	
def geonames_uri(x):
    "Return a URI for Geonames.org features."
    return 'http://data.usgs.gov/geonames/' + x



